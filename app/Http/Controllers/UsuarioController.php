    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $cont = 1;

        $usuarios = User::orderBy('created_at', 'desc')->paginate(8);

        return view('backend.usuarios.index', [
            'usuarios' => $usuarios,
            'cont' => $cont,
        ]);
    }

